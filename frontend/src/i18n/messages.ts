export type CompleteLocale = (typeof completeLocales)[number];

export const fallbackLocale: CompleteLocale = 'en';

export const messages: Record<CompleteLocale, Record<MessageKey, string>> = {
  en: enMessages,
  de: deMessages,
  fr: frMessages
};

export function isCompleteLocale(locale: string): locale is CompleteLocale {
  return completeLocales.includes(locale as CompleteLocale);
}

export function hasMessageKey(key: string): key is MessageKey {
  return Object.prototype.hasOwnProperty.call(enMessages, key);
}
