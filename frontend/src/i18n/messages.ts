export type MessageKey = keyof typeof enMessages;

export const completeLocales = ['en', 'de', 'fr', 'es', 'it', 'nl', 'pl'] as const;
export type CompleteLocale = (typeof completeLocales)[number];

export const fallbackLocale = 'en' as const;

export type LocaleMessages = Record<MessageKey, string>;

// English is bundled synchronously so a complete message set is always available
// as the fallback. The other locales are loaded on demand via dynamic import() so
// they are emitted as separate chunks instead of bloating the main bundle.
const localeLoaders: Record<Exclude<CompleteLocale, 'en'>, () => Promise<LocaleMessages>> = {
  de: () => import('./locales/de').then((module) => module.default),
  fr: () => import('./locales/fr').then((module) => module.default),
  es: () => import('./locales/es').then((module) => module.default),
  it: () => import('./locales/it').then((module) => module.default),
  nl: () => import('./locales/nl').then((module) => module.default),
  pl: () => import('./locales/pl').then((module) => module.default)
};

export function loadLocaleMessages(locale: CompleteLocale): Promise<LocaleMessages> {
  if (locale === fallbackLocale) return Promise.resolve(enMessages);
  return localeLoaders[locale]();
}

export function isCompleteLocale(locale: string): locale is CompleteLocale {
  return completeLocales.includes(locale as CompleteLocale);
}

export function hasMessageKey(key: string): key is MessageKey {
  return Object.prototype.hasOwnProperty.call(enMessages, key);
}
